            <h2 class="article-title">トゥーリングの着用の意味</h2>
            <p>インドでは、2番目の指に既婚者の証として着用されていたトゥーリングですが、現在では着用する指も自由で、指輪やピアス・ネックレスと同じようにファッションの一部として楽しまれています。日本や欧米諸国でも、結婚式での指輪の交換や婚約指輪を左手の薬指に着用する習慣があります。
            これは、古代ギリシャ神話で薬指は心臓と直接つながっている血管が流れていると信じられてきたため、永遠の誓いの意味をこめて薬指に婚約指輪や結婚指輪をはめるようになったと言われています。同じように足の2番目の指も特別な指であると考えられています。</p>
        </li>

        <li>
            <h2 class="article-title">トゥーリングの素材</h2>
            <p>ヒンズー教では、金は非常に貴い金属として扱われ人々は腰よりも低いところには金を着用する習慣がなかったので、インドでは素材は主にシルバーを使用していたそうですが、現代ではシルバーに限らず18金なども多く使われているようです。インド以外の国でも日常のジュエリーとしてトゥーリングが普及しはじめ、硬い金属だけではなく指にあたっても痛くないやわらかいラバーを使用したものも登場しています。</p>
        </li>

