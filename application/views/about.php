            <h2 class="article-title">トゥーリングの起源</h2>
            <p>古代インドの大長抒情詩【ラーマーヤナ】では、魔王ラヴァーナに連れ去られたシータという女性が、連れ去られる途中、主人のラーマに見つけ出してもらえるようにと身に着けていたトゥーリングを道に投げたそうです。裸足文化のインドでは古くから今に至るまで結婚式の際に花婿は花嫁の両足の2番目の指に結婚の証としてトゥーリングを贈る習慣があります。そしてそのトゥーリングはヒンドゥー語 でビチヤ（Bichiya)と呼ばれています。</p>
            <p>インドでは心臓や子宮と足の2番目の指が繋がっていて、この指にトゥーリングを着けることで体のめぐりが良くなると信じられていたそうです。着用されたトゥーリングは一生はずさず既婚女性の証としてります。
            また、エジプトでは3300年以上前の古代遺跡（集団墓地のようなところ）の中からの銅合金のトゥーリングを着用したミイラが2013年に発見され、話題となりました。遺体の足の骨に損傷があったことから、装飾品としてではなく足の怪我を治す為のお守りのような意味で着用されていたのではないかと推測されているようです。</p>
            <p>トゥーリングの起源はまだ謎に包まれている部分はありますが、インドのビチア（Bichiya)が世界に広がったともみられています。</p>
        </li>
